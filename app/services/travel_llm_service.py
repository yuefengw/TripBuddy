    @staticmethod
    def _format_history(session_history: Sequence[Dict[str, Any]], *, limit: int = 6) -> str:
        if not session_history:
            return "No previous turns."

        items: List[str] = []
        for entry in session_history[-limit:]:
            role = entry.get("role", "unknown")
            content = str(entry.get("content", "")).strip().replace("\n", " ")
            items.append(f"{role}: {content[:240]}")
        return "\n".join(items)

    @classmethod
    def _coerce_text(cls, content: Any) -> str:
        if content is None:
            return ""
        if isinstance(content, str):
            return content
        if isinstance(content, list):
            return "".join(cls._coerce_text(item) for item in content)
        if isinstance(content, dict):
            if "text" in content:
                return cls._coerce_text(content["text"])
            if "content" in content:
                return cls._coerce_text(content["content"])
            if "output_text" in content:
                return cls._coerce_text(content["output_text"])
            return ""
        text_attr = getattr(content, "text", None)
        if text_attr is not None:
            return cls._coerce_text(text_attr)
        content_attr = getattr(content, "content", None)
        if content_attr is not None and content_attr is not content:
            return cls._coerce_text(content_attr)
        if isinstance(content, Iterable) and not isinstance(content, (bytes, bytearray)):
            return "".join(cls._coerce_text(item) for item in content)
        return str(content)


